# This program converts degrees Fahrenheit to degrees Celsius

# Get user input
print("Enter the Fahrenheit degrees to convert: ")
fahrenheit = int(input("--> "))

# Calculating the Celsius degrees
celsius = (fahrenheit - 32) * 5/9

# Printing the answer
print(f"{fahrenheit} degrees in Fahrenheit are {celsius} Celsius degrees.")
